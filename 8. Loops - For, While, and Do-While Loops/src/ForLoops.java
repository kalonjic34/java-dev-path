public class ForLoops {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("This is after the loop\n");
        
        
        for (int i = 10; i > 0; i--) {
            System.out.println(i);
        }
    }
} 