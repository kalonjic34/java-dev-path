public class PrintVsPrintln {
    public static void main(String[] args) {
        System.out.println("Hello, World");
        System.out.print("Hello, Chris");

        System.out.println();
        System.out.println();

        // Escape Sequence with \n
        System.out.println("Hello, \nWorld");
        System.out.println("Welcome");
    }
}