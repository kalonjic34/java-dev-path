public class NestedIfElse {
    public static void main(String[] args) {
        // nested  if else control statement
        
        int number = -5;
        if (number > 0) {
            System.out.println("Number is positive");
        }else if(number < 0){
            System.out.println("Number is negative");
        }else{
            System.out.println("Number is 0");
        }
    }
}