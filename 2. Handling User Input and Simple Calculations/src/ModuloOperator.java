public class ModuloOperator {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 8;
        int result;

        // Addition
        result = num1 + num2;
        System.out.print("Addition result: "+result);

        // Substraction
        result = num1 - num2;
        System.out.print("\nSubstraction result: "+result);

        // Multiplication
        result = num1 * num2;
        System.out.print("\nMultiplication result: "+result);

        // Division
        result = num1 / num2;
        System.out.print("\nDivision result: "+result);

        // Modulo
        result = num2 % num1;
        System.out.println("\nModulo result: "+result);

    }
}